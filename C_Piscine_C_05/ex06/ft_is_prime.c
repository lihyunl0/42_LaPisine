int		ft_is_prime(int nb)
{
	int i;
	int temp;

	if (nb <= 1)
		return (0);
	temp = nb / 2;
	i = 2;
	while (i <= temp)
	{
		if (nb % i == 0)
			return (0);
		i++;
	}
	return (1);
}
